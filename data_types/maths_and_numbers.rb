# Add
puts 5 + 10
# 15

# Div
puts 10 / 2
# 5s

# Power
puts 10 ** 2
# 100

# Modulo
puts 10 % 3
# Remainder: 1

# absolute value
charge = -10.56
puts charge.abs
# 10.56

# Round
amount = 19.899
puts amount.round
# 20


